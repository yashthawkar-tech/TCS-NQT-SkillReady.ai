#tuple slicing
tuple = (10,20,30,40,50)
#        0  1  2  3  4
print(tuple[1:4])#it will print 2nd to 4th element of the tuple
print(tuple[0:4])#it will print 1st to 4th element of the tuple
print(tuple[2:])#it will print 3rd to last element of the tuple
print(tuple[:4])#it will print 1st to 4th element of the tuple
print(tuple[:-1])#it will print 1st to 4th element of the tuple


