data = [1,2,3,4,5,6]
print(data[-1])#it will return the last element (it starts from the end)
print(data[:])#print all values 
print(data[::-1])#it will reverse the list
print(data[:-1])#it will not print the last element(it will skip it)
print(data[:-2])# it will skill last two elemets
print(data[::-2])#it will take step and skip 2nd number in reverse 

